import { ImageSourcePropType } from "react-native";

export type NewsItem = {
  id: string;
  title: string;
  summary: string;
  time: string;
  category: string;
  image?: ImageSourcePropType;
  body?: string[];
};

export type FixtureItem = {
  id: string;
  opponent: string;
  date: string;
  time: string;
  venue: string;
  competition: string;
  isHome: boolean;
  status?: "upcoming" | "live" | "finished";
  score?: string;
};

export type ExternalClip = {
  platform: "youtube" | "bein" | "trt" | "x" | "instagram";
  provider: string;
  url: string;
  embedUrl?: string;
  note?: string;
};

export type LiveEvent = {
  id: string;
  minute: number;
  team: "home" | "away";
  type: "goal" | "card" | "var" | "sub";
  player: string;
  detail: string;
  clip?: ExternalClip;
  videoUrl?: string;
  thumb?: ImageSourcePropType;
  thumbUrl?: string;
};

export type StandingRow = {
  pos: number;
  team: string;
  mp: number; // Oynanan (Played)
  w: number;  // Galibiyet (Won)
  d: number;  // Berabere (Drawn)
  l: number;  // Mağlubiyet (Lost)
  gf: number; // Atılan Gol (Goals For)
  ga: number; // Yenilen Gol (Goals Against)
  gd: number; // Averaj (Goal Difference)
  pts: number; // Puan (Points)
  positionChange?: number; // +1, -2, 0 etc. for arrows
  logo?: string; // Team logo URL or placeholder
};

export type Legend = {
  id: string;
  name: string;
  role: string;
  years: string;
  highlight: string;
};

export type Player = {
  id: string;
  name: string;
  position: string;
  number: number;
  age: number;
  height: string;
  foot: "Right" | "Left" | "Both";
  bio: string;
  strengths: string[];
  hometown?: string;
  matches?: number;
  goals?: number;
  assists?: number;
  rating?: number;
  career?: string[];
  image?: ImageSourcePropType;
};

export type SocialMedia = {
  instagram?: string;
  twitter?: string;
};

export type ManagementMember = {
  id: string;
  name: string;
  role: string;
  bio: string;
  photoUrl?: string;
  socialMedia?: SocialMedia;
};

export type CoachingStaffMember = {
  id: string;
  name: string;
  role: string;
  bio: string;
  photoUrl?: string;
  socialMedia?: SocialMedia;
};

export type PreviousClub = {
  club: string;
  years: string;
};

export type SquadPlayer = {
  id: string;
  name: string;
  jerseyNumber: number;
  position: string; // "Kaleci", "Defans", "Orta Saha", "Forvet"
  detailedPosition: string; // e.g., "Orta Saha - Merkez", "Defans - Sol Bek"
  age: number;
  birthDate: string; // e.g., "15 Mart 1995"
  birthPlace: string; // City name
  nationality: string;
  height: string; // e.g., "1.88 m"
  weight: string; // e.g., "82 kg"
  preferredFoot: "Sol" | "Sağ" | "Çift Ayak";
  marketValue: string; // e.g., "€1.200.000"
  currentClub: string; // "Amedspor" or "Amed SK"
  previousClubs: PreviousClub[];
  bio: string;
  photoUrl?: string;
  socialMedia?: SocialMedia;
};

export type KitItem = {
  id: string;
  season: string;
  title: string;
  palette: string;
  note: string;
  colors?: string[];
  image?: ImageSourcePropType;
};

export type PollOption = { id: string; text: string; votes: number };

export type Poll = {
  id: string;
  question: string;
  closesIn: string;
  options: PollOption[];
};

export type Announcement = {
  id: string;
  title: string;
  city: string;
  location: string;
  date: string;
  link?: string;
  contact: string;
  note: string;
  status?: "pending" | "approved" | "rejected";
};

export type FanMoment = {
  id: string;
  user: string;
  location: string;
  caption: string;
  time: string;
  timestamp?: string;
  source: "Tribun" | "Sehir Meydani" | "Ev/Izleme";
  image?: ImageSourcePropType;
};

export type MatchResult = {
  id: string;
  week: number;
  date: string;
  homeTeam: string;
  awayTeam: string;
  homeScore: number;
  awayScore: number;
  competition?: string;
};

export type UpcomingMatch = {
  id: string;
  week: number;
  date: string;
  time: string;
  homeTeam: string;
  awayTeam: string;
  venue?: string;
  competition?: string;
};

export type LeagueLegendItem = {
  id: string;
  color: string;
  label: string;
  description: string;
  positions?: string; // e.g., "1-2" or "3-5"
};

export type ChatMessage = {
  id: string;
  text: string;
  sender: string;
  timestamp: string;
  isMine?: boolean;
  isRead?: boolean;
};

export type MatchRoomPhase = "pre" | "post";

export type MatchRoom = {
  id: string;
  title: string;
  time: string;
  phase: MatchRoomPhase;
  status?: "upcoming" | "live" | "finished";
  messages: ChatMessage[];
};

